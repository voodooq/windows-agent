def get_language_names() -> dict[str, str]:
    return {
        code: data.get("meta", {}).get("language_name", code)
        for code, data in TRANSLATIONS.items()
    }


def translate(language: str, *keys: str, default: str = "") -> Any:
    data: Any = TRANSLATIONS.get(language) or TRANSLATIONS["zh-CN"]
    for key in keys:
        if not isinstance(data, dict):
            return default
        data = data.get(key)
        if data is None:
            return default
    return data